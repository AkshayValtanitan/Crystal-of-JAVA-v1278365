package Components.enums;

/**
 * Enum representing the different types of text alignment options.
 * <p>
 * This enum defines four types of text alignment:
 * <ul>
 *   <li><b>LEFT</b> - Aligns text to the left.</li>
 *   <li><b>RIGHT</b> - Aligns text to the right.</li>
 *   <li><b>CENTER</b> - Aligns text to the center.</li>
 *   <li><b>JUSTIFY</b> - Aligns text with both the left and right edges, adjusting spacing between words.</li>
 * </ul>
 * </p>
 */
public enum Alignment {
    LEFT,    // Aligns text to the left
    RIGHT,   // Aligns text to the right
    CENTER,  // Aligns text to the center
    JUSTIFY  // Aligns text with both the left and right edges, adjusting spacing between words
}
