package Skills;

/**
 * Enum representing the types of skills available.
 * <ul>
 *     <li>PHYSICAL - Skills involving physical strength or actions.</li>
 *     <li>MAGIC - Skills involving magical abilities or spells.</li>
 * </ul>
 */
public enum SkillType {
    /**
     * Skills based on physical strength or actions.
     */
    PHYSICAL,

    /**
     * Skills based on magical abilities or spells.
     */
    MAGIC,
}
